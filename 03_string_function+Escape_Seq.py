# String Functions

DS = "Hello Friends I Am Sanchit, I like Coding Very Much !, I am 17een Years Old And i Have a Better Knowledge About Python, But Currently I Am Study Python Deeply Bcos I Am Intrested !"

print(len(DS))           # => It Show The Length Of String in Number i.e Number Of Alphabets! = 180
print(DS.endswith("!"))  # => This Function Use To Verify The String Is end With That Word ! It Show True Otherwise False !
print(DS.count('I'))     # => It Calculate The Number Of Any Word In String, It Show 6 As Answer !
print(DS.capitalize())   # => It Will Capital The First Alphabet Of String, In DS String It Capitalize "H" Word 
print(DS.find("Coding")) # => It Will Find Only First Position (in Numbers) Of Word From The String, It Show '35' ; Otherwise It Show '-1' (If Word Not Exist in String)
print(DS.replace("I", "Me")) # => It Replace The Old Word With New Word !  

